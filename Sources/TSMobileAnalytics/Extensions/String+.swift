import Foundation

extension String {
    static let slash = "/"
    static let empty = ""
    static let newLine = "\n"
    static let space = " "
    static let colon = ":"
    static let dot = "."
    static let questionMark = "?"
    static let ampersand = "&"
    static let percent = "%"
    static let equals = "="
    static let openParenthesis = "("
    static let closingParenthesis = ")"
    static let raised = "^"
    static let indentation: String = space + space + space
}
